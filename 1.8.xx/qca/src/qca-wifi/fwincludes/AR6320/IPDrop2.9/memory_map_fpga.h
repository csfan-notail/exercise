#define KBYTE 1024
#define MBYTE (KBYTE*KBYTE)
/*wlan cpu*/
#define WLAN_CPU_DPORT_BASE 0x400000 
#define WLAN_CPU_IPORT_BASE 0x900000

#define WLAN_CPU_DRAM_BASE  0x400000
#define WLAN_CPU_DRAM_BLOCK_SIZE (32*KBYTE)
