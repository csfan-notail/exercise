/*non-pcie mode*/
/*when in pcie mode, intf_ram = 128K, starting from 0xA0000, */
#define WLAN_INTF_RAM_NON_PCIE_BASE 0xA0000
#define WLAN_INTF_RAM_NON_PCIE_SIZE (128*KBYTE)

/*when in pcie mode, intf_ram = intf_ram1 + intf_ram2, */
/*intf_ram1=96k starting from 0xA0000, access it through intf controller*/
/*intf_ram2=32k starting from 0x468000, access it through dram (cpu inbound)*/
/*pcie mode ram1*/
#define WLAN_INTF_RAM1_PCIE_BASE 0xA0000 
#define WLAN_INTF_RAM1_PCIE_SIZE (96*KBYTE)
/*pcie mode ram2*/
#define WLAN_INTF_RAM2_PCIE_BASE 0x468000
#define WLAN_INTF_RAM2_PCIE_SIZE (32*KBYTE)
/*for shared memory*/
