AP_ERROR_CODE_E gpioex_set_debugLevel(UINT32 uiDebugLevel)
{
    if (uiDebugLevel > GPIOEX_LOG_DEBUG)
    {
        return AP_E_PARAM;
    }
    g_enDebugLevel = uiDebugLevel;
    return AP_E_NONE;
}

UINT32 gpioex_get_debugLevel(void)
{
    return g_enDebugLevel;
}



