void ar9344_gpio_config_input(int gpio)
{
    ar9344_reg_rmw_set(AR9344_GPIO_OE, (1 << gpio));
}

void ar9344_gpio_config_output(int gpio)
{
    ar9344_reg_rmw_clear(AR9344_GPIO_OE, (1 << gpio));
}
int ar9344_gpio_in_val(int gpio)
{
    return((1 << gpio) & (ar9344_reg_rd(AR9344_GPIO_IN)));
}
void  ar9344_gpio_drive_low(unsigned int mask)
{
  ar9344_reg_wr(AR9344_GPIO_CLEAR, mask);
  //ar9344_reg_rmw_clear(AR9344_GPIO_OE, mask);
}

void ar9344_gpio_drive_high(unsigned int mask)
{
  ar9344_reg_wr(AR9344_GPIO_SET, mask);
  //ar9344_reg_rmw_clear(AR9344_GPIO_OE, mask);
}


