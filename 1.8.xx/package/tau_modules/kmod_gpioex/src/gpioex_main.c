AP_ERROR_CODE_E gpioex_init(void)
{
    UINT32 uiRd = 0;



    /*DISABLE JTAG*/
    ar9344_reg_rmw_set(AR9344_GPIO_FUNCTIONS,0x00000002);

#if 1
    /*片选GPIO EXP :GPIO 2*/
    ar9344_gpio_config_output(2);

    uiRd = ar9344_reg_rd(AR9344_GPIO_OUT_FUNCTION0);
    uiRd = (uiRd &(~(0x00ff0000)))|0x000A0000;
    ar9344_reg_wr_nf(AR9344_GPIO_OUT_FUNCTION0,uiRd);
#endif    
    /* 先配置状态，后配置模式，解决由于内部寄存器翻转导致优先配置mode会导致设
       备重启的bug */


    return AP_E_NONE;
}

